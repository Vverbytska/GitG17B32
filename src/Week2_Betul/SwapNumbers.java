package Week2_Betul;

public class SwapNumbers {

    public static void main(String[] args) {

        int a = 8;
        int b = 10;

        a = a + b; // 8 + 10 ==> a=18
        b = a - b; // 18 - 10 ==> b=8
        a = a - b; // 18 - 8 == a=10

        System.out.println(a);
        System.out.println(b);





    }



}
